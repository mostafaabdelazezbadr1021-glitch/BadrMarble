let currentLang = localStorage.getItem('badr_lang') || 'ar';
const langBtn = document.getElementById('langBtn');

function updateLanguage(lang) {
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
        document.documentElement.lang = 'ar';
        langBtn.innerText = "English";
    } else {
        document.documentElement.dir = 'ltr';
        document.documentElement.lang = 'en';
        langBtn.innerText = "العربية";
    }

    document.getElementById('siteTitleTag').innerText = translations[lang].siteTitle;

    const keys = Object.keys(translations[lang]);
    keys.forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            
            if (translations[lang][key].includes('<') || translations[lang][key].includes('>')) {
                element.innerHTML = translations[lang][key];
            } else {
                element.innerText = translations[lang][key];
            }
        }
    });

    document.querySelectorAll('.data-translate-cat').forEach(el => {
        el.innerText = el.getAttribute(`data-${lang}`);
    });
    document.querySelectorAll('.data-translate-name').forEach(el => {
        el.innerText = el.getAttribute(`data-${lang}`);
    });
    document.querySelectorAll('.lbl-view-details').forEach(el => {
        el.innerText = translations[lang].viewDetails;
    });

    localStorage.setItem('badr_lang', lang);
}

langBtn.addEventListener('click', () => {
    currentLang = currentLang === 'ar' ? 'en' : 'ar';
    updateLanguage(currentLang);
});

document.addEventListener('DOMContentLoaded', () => {
    updateLanguage(currentLang);
});


