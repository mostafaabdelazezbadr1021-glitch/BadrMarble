document.addEventListener("DOMContentLoaded", function() {
    const urlParams = new URLSearchParams(window.location.search);
    const incomingCodes = urlParams.get('codes');
    const currentLang = urlParams.get('lang') || 'ar';
    
    if (incomingCodes) {
        const textarea = document.getElementById('projectDetails'); 
        
        if (textarea) {
            if(currentLang === 'en') {
                textarea.value = `Selected Products Codes: [ ${incomingCodes} ]\n\n`;
            } else {
                textarea.value = `أكواد المنتجات التي تم اختيارها: [ ${incomingCodes} ]\n\n`;
            }
        }

        const nameInput = document.getElementById('custName');
        if (nameInput) {
            nameInput.focus();
        }

        const cleanUrl = window.location.pathname + window.location.hash; 
        window.history.replaceState({}, document.title, cleanUrl);

        const orderSection = document.getElementById('order');
        if (orderSection) {
            setTimeout(() => {
                orderSection.scrollIntoView({ behavior: 'smooth', block: 'center' });
            }, 300); 
        }
    }
});
